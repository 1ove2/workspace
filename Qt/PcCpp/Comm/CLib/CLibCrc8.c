/*---------------------------------- 预处理区 ---------------------------------*/

/************************************ 头文件 ***********************************/
#include "CLibCrc8.h"


#define _CLIB_CRC8_CODE                   (0x31)
/*----------------------------------- 声明区 ----------------------------------*/

/********************************** 变量声明区 *********************************/


/********************************** 函数声明区 *********************************/


/********************************** 变量实现区 *********************************/


/********************************** 函数实现区 *********************************/
u8 CLibCrc8(const u8 *ptr, size_t len)
{
    u8 i;
    u8 crc = 0x00;

    while(len--)
    {
        crc ^= *ptr++;
    
        for (i=8; i>0; --i)
        {
            if (crc & 0x80)
            {
                crc = (crc << 1) ^ _CLIB_CRC8_CODE;
            }
            else
            {
                crc = (crc << 1);
            }
        }
    }
    
    return (crc);
}
